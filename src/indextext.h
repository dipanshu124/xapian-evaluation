#ifndef _INDEXTEXT_H_
#define _INDEXTEXT_H_

#include <ctype.h>
#include <string>

static inline void
lowercase_term(std::string &term)
{
    string::iterator i = term.begin();
    while (i != term.end()) {
	*i = tolower(*i);
	i++;
    }
}

#endif
